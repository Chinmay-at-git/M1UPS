    private enum State{E_INIT, E_AFFICHER, E_MODIFIER};
    private DefaultListModel<Enregistrement> liste;
    private State currentState;
    private Enregistrement currentEnr, tmpEnr;
    
    public void addElement(){
        if(isCorrect()){
            currentEnr = new Enregistrement(vTitre.getText(), 1);
            liste.addElement(currentEnr);
        }
        
    }
    public void removeElement(){
        liste.removeElement(currentEnr);
    }
    public void remplacerElement(Enregistrement e){
        if(isCorrect()){
            tmpEnr = currentEnr;
            for(int i=0; i < liste.size(); i++)
            if(liste.get(i).getName().equals(currentEnr.getName())){
                liste.set(i, e);
                currentEnr = e;
                break;
            } 
        }  
    }
    public void retablirElement(){
        if(tmpEnr != null){
            for(int i=0; i < liste.size(); i++)
                if(liste.get(i).getName().equals(currentEnr.getName())){
                        liste.set(i, tmpEnr);
                        currentEnr = tmpEnr;
                        break;
                 }
        }
        
    }
    public boolean isCorrect(){
        if(vTitre.getText().isEmpty() && (!val1.isSelected()
                || !val2.isSelected() || !val3.isSelected()))
            return false;
        return true;
    }
    public void activation(){
        switch(currentState){
            case E_INIT:
                bAjouter.setEnabled(true);
                bSupprimer.setEnabled(false);
                bRemplacer.setEnabled(false);
                bRetablir.setEnabled(false);
                break;
            case E_AFFICHER:
                bAjouter.setEnabled(false);
                bSupprimer.setEnabled(true);
                bRemplacer.setEnabled(true);
                bRetablir.setEnabled(true);
                break;
            case E_MODIFIER:
                bAjouter.setEnabled(false);
                bSupprimer.setEnabled(false);
                bRemplacer.setEnabled(true);
                bRetablir.setEnabled(true);
                break;
        }
    }
