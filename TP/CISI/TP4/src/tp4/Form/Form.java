    private void bSupprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSupprimerActionPerformed
        // TODO add your handling code here:
        switch(currentState){
            case E_INIT:
                throw new RuntimeException("E_INIT INTERDIT");
            case E_AFFICHER:
                if(liste.size() > 1){
                    currentState = State.E_AFFICHER;
                    removeElement();
                    activation();
                }else if(liste.size() == 1){
                    currentState = State.E_INIT;
                    removeElement();
                    activation();
                }
                break;
            case E_MODIFIER:
                throw new RuntimeException("E_MODIFIER INTERDIT");
        }
    }//GEN-LAST:event_bSupprimerActionPerformed

